class Product {
    constructor(name, code, ID, price, seller, country, type, date, category, desript, images) {
        this.name = name
        this.code = code
        this.ID = ID
        this.price = price
        this.seller = seller
        this.country = country
        this.type = type
        this.date = date
        this.category = category
        this.desript = desript 
        this.images = images
    }
}

module.exports = Product
